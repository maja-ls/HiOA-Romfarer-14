                    <h2>GLIESE 436 B</h2>

                    <figure class="planetBilde">
                        <img src="filer/bilder/hoticeNasa.jpg" alt="Et bilde som viser planeten Gliese 436b, planeten er laget av is og dekket av flammer" title="Dette bildet er &copy;NASA">
                        <figcaption>Planeten Gliese 436b</figcaption>
                    </figure>

                    <div class="planetTekst">
                        <p>Denne planeten kan du finne i stjernebildet Løven og den ligger ca 33 lysår fra jorden.
                            Den går rundt stjernen sin på 2 dager og 15.5 timer.
                            <br/>
                            Denne planeten er ganske nærme stjernen sin, noe som gjør at den har en veldig høy temperatur, 
                            ca 439 grader celsius. Men det som er så fascinerende med denne planeten er at selv om den har 
                            en så høy temperatur, så er den laget av is!
                            <br/>
                            Det utrolig høye trykket som er på planeten grunnet veldig høy tyngdekraft drar vannmolekylene inn mot kjernen og pakker det så tykt sammen
                            at den er en solid masse av is.
                            <br/>
                            Samtidig som overflaten brenner skaper dette konseptet ‘hot ice’, eller brennende is.</p>
                        <p>På denne planeten kan man ta inn på hotell for kun 200 000,00 kr. per natt.
                            <br/>
                            Du kan dra ut på guidede turer i et robotkjøretøy for å se på den brennende isen.
                            <br/>
                            Betaler du 500 000,00 kr. ekstra kan du overnatte i en liten leir som er slått opp midt i det mest turbulente brennende området.
                            Dette kan være en sann opplevelse for livet.</p>
                        <p>Ellers har hotellet det meste som kreves av fasiliteter. Her er det gourmetrestauranter, svømmehall, tennis, squash og badmintonbane. 
                            Det er også en rekke butikker her, så de som  velger å bo på planeten i lengre perioder kan gjøre det. Butikkene får varer sendt
                            med skipet fra Jorda.</p>


