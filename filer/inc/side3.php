                    <div class="planetTekst">
                        <p>Dette er en magnetisert nøytronstjerne som sender ut lys med regelmessige mellomrom. Samtidig som den 
                            sender ut lys roterer den opp til 43000 ganger i minuttet. Den sender ut masser av røntgen-stråler og en hel haug med radiostråling, 
                            noe som er veldig annerledes sammenlignet med andre stjerner.</p>
                        <p>Dette reisemålet har ikke noe tilgjengelig hotell, ettersom det ikke har en fast masse og uansett ville vært for farlig å slå seg ned på.
                            I stedet har de tilreisende mulighet til å beskue pulsaren fra egne observasjonsrom hvor de blir vartet opp med drinker og mat.
                            <br/>
                            Skipet ligger i nærheten av pulsaren i to døgn, og selve 'pulsar-turen' koster 3 000 000,00 kr.</p>
                        <p>Reisemålet er også unikt i det at det tilbyr en noe "unik" tjeneste. For den nette sum av 
                            1 000 000 000,00 kr kan du gjøre slutt på ditt liv ved å brenne opp fra innsiden av pulsarens stråling.
                            <br/> 
                            Du får en egen gullkiste som du legges i, og så får du kontroll over en knapp som åpner et beskyttelsesdeksel slik at den 
                            dødelige strålingen slipper inn. Ta kvelden med stil!</p>


