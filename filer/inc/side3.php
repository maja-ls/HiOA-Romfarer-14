                    <h2>PULSAR</h2>

                    <figure class="planetBilde">
                        <img src="filer/bilder/PulsarNasa.jpg" alt="Et bilde som viser en Pulsar, og det dansende lyset som stjernen sender ut" title="Dette bildet er &copy;NASA">
