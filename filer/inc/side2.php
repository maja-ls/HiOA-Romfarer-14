                        <p>Poseidon-I er selve flaggskipet til Romfarer-14. Med plass til 20 individuelle gjester og en hel del par- og til og med familierom, er Poseidon-I klar til å takle
                            selv de mest avanserte romreiseønsker.
                            <br/>
                            Utformingen av skipet er inspirert av triangulære UFOer som har blitt observert flere steder i verden, og er en stadig påminnelse
                            om at med interstellare reiser kan man når som helst bli den første til å gjøre kontakt med utenomjordiske.</p>

                        <p>Ettersom avstanden som skal traverseres er enorm tar reisen noe lenger tid enn med Hermes-I, men dette mener vi er verdt det. Gjennom dine anslagsvis 5-20 dager på skipet
                            vil du kunne nyte gourmetmåltider basert på molekylær gastronomi i vår restaurant. Her regner intet annet enn skipets superdatamaskin ikke bare ut nye smakskombinasjoner,
                            men tar også i bruk genetisk informasjon om deg til å finne ut akkurat hvilke smaker du vil foretrekke. Enkelte får hjemlengsel og blir engstelige av romoppholdet, derfor
                            har vi også et kriselager med pølser og  pølsebrød. En kjent og kjær jordisk rett!</p>

                        <p>Av andre fasiliteter har skipet svømmehall, kino, kunstig tyngdekraft med muligheter for å slås av i ditt eget soverom, treningsrom, bowlingbane og bar.
                            I sistnevnte er antallet drinker per 6 timer begrenset til tre, av sikkerhetsårsaker.</p>
